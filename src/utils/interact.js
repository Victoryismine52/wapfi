  export const mintNFT = async(amount) => {
    //error handling
    if (amount > 2 || amount < 0.1 ) { 
        return {
            success: false,
            status: "Please enter an amount between .1 and 2",
        }
    }

    //load smart contract
    window.contract = await new window.web3.eth.Contract(contractABI, contractAddress);//loadContract();
    //set up your Ethereum transaction
    const transactionParameters = {
        to: contractAddress, // Required except during contract publications.
        from: window.ethereum.selectedAddress, // must match user's active address.
        'data': window.contract.methods.mintWAPFIDutchAuction(amount).encodeABI() //make call to NFT smart contract 
    };



    //sign transaction via Metamask
    try {
        const txHash = await window.ethereum
            .request({
                method: 'eth_sendTransaction',
                params: [transactionParameters],
            });
        return {
            success: true,
            status: "✅ Check out your transaction on Etherscan: https://ropsten.etherscan.io/tx/" + txHash
        }
    } catch (error) {
        return {
            success: false,
            status: "😥 Something went wrong: " + error.message
        }
    }
}
